    # 选择驱动
    def chooseEdge(self):
        self.logsAppend('选择Egde浏览器驱动')
        caps = {
            "browserName": "MicrosoftEdge",
            "version": "",
            "platform": "WINDOWS",
            "ms:edgeOptions": {
                "extensions": [],
                "args": []
            }
        }
        caps["ms:edgeOptions"]["args"].append("--start-maximized")
        # 规避检测
        caps["ms:edgeOptions"]["args"].append("--disable-blink-features=AutomationControlled")
        # 无界面
        if self.ui.check_interface.isChecked():
            caps["ms:edgeOptions"]["args"].append("--headless")
            caps["ms:edgeOptions"]["args"].append("--disable-gpu")
        # 不留痕
        if self.ui.check_notEvidence.isChecked():
            caps["ms:edgeOptions"]["args"].append("--incognito")
        # 代理IP
        proxy = self.ui.le_proxy.text()
        if proxy != "":
            caps["ms:edgeOptions"]["args"].append("--proxy-server={0}".format(proxy))
        if browser.webdriverPath != "":
            try:
                browser.driver = webdriver.Edge(executable_path=browser.webdriverPath, capabilities=caps)
                precondition.driving_Conditions = 1
            except Exception as e:
                self.logsAppend('请选择正确的驱动   \n{0}'.format(e))
        else:
            self.logsAppend('请选择驱动')

    def chooseChrome(self):
        self.logsAppend('选择Chrome浏览器驱动')
        options = Options()
        # 无界面
        if self.ui.check_interface.isChecked():
            options.add_argument("--headless")
            options.add_argument("--disable-gpu")
        # 不留痕
        if self.ui.check_notEvidence.isChecked():
            options.add_argument("--incognito")
        # 代理IP
        proxy = self.ui.le_proxy.text()
